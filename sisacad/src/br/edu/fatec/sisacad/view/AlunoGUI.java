    private void buttonIncluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonIncluirMouseClicked
        // TODO add your handling code here:
        Aluno a = new Aluno();
        a.setNome(txtNome.getText());
        //Instanciar negocios
        AlunoBUS bus = new AlunoBUS(a);
        txtRa.setText(Integer.toString(a.getRa()));
        boolean status = bus.incluir(a);
        if (status) {
            JOptionPane.showMessageDialog(null, "Incluido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Favor Verificar o campo nome");
        }
    }//GEN-LAST:event_buttonIncluirMouseClicked

